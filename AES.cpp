array<Byte, 16> AES128Enc(array<Byte, 16> Input, array<Byte, 16> Key) {
  // key scheduling prepare
  vector<array<Byte,16>> K;//key
  K.push_back(Key);
  int keyround;
  for(keyround = 1; keyround < 11; keyround++){
    K.push_back(KeyExpand(K[keyround-1],keyround));
  }
  // round 0-10
  int i;
  array<Byte, 16> SBB;//subbyte
  array<Byte, 16> SHR;//shift rows
  array<Byte, 16> MXC;//mix columns
  array<Byte, 16> ARK;//add round key
  for(i=0; i<=10; i++){
    if(i==0){//round 0
      ARK=AddKey(Input,K[0]);
    }
    else{
      if(i>0&&i<10){//round 1-9
        SBB = SubByte(ARK);
        SHR = shiftrows(SBB);
        MXC = MixCol(SHR);
        ARK = AddKey(MXC,K[i]);
      }else{//round 10
        SBB = SubByte(ARK);
        SHR = shiftrows(SBB);
        ARK = AddKey(SHR,K[i]);
      }
    }
  }
  // return
  return ARK;
}
