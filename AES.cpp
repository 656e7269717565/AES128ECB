array<Byte, 16> AES128Dec(array<Byte, 16> input, array<Byte, 16> Key) {  // low key, this was the most difficult function to write, don't ask me how XD
  // keys
  /*
  IARK
  IMXC
  ISHR
  ISSB
  */
  vector<array<Byte,16>> K;//key
  K.push_back(Key);
  int keyround;
  for(keyround = 1; keyround < 11; keyround++){
    K.push_back(KeyExpand(K[keyround-1],keyround));
  }
  // round 0-10
  int i;
  array<Byte, 16> ISBB;//inverse subbyte
  array<Byte, 16> ISHR;//inverse shift rows
  array<Byte, 16> IMXC;//inverse mix columns
  array<Byte, 16> IARK;//inverse add round key(which is the same as add round key)
  for(i=10;i>=0;i--){
    if(i==10){//round 10
      IARK = AddKey(input,K[i]);
      ISHR = InvShiftRows(IARK);
      ISBB = ISbox(ISHR);
    }
    else{
      if(i<10&&i>0){//round 9-1
        IARK = AddKey(ISBB,K[i]);
        IMXC = InvMixCol(IARK);
        ISHR = InvShiftRows(IMXC);
        ISBB = ISbox(ISHR);
      }else{//round 0
        IARK = AddKey(ISBB,K[i]);
      }
    }
  }
  return IARK;
}

vector<array<Byte,16>> string2blocks(string input){
    int size = input.length();
    int blocks = size/16;
    int remain = size%16;
    int i;
    int j;
    vector<array<Byte,16>> Blocks;
    array<Byte,16> temp;
    for( i = 0; i < blocks; i++){
        for(j = 0; j<16; j++){
            temp[j] = (Byte)input[i*16+j];
        }
        Blocks.push_back(temp);

    }
    for(j=0;j<16;j++){
      if(j<remain){
        temp[j]=(Byte)input[i*16+j];
        }
      else{
        temp[j]=0x00;
      }
        
    }
    Blocks.push_back(temp);
    return Blocks;
}

